# Задание.4
import random  # Подключаем модуль для генерации случайных чисел
errors = 0  # Счетчик ошибок
correct = 0  # Счетчик правильных ответов

# Цикл работает, пока ошибок меньше 3
while errors < 3:
    # Генерируем два случайных числа от 1 до 10
    num1 = random.randint(1, 10)
    num2 = random.randint(1, 10)

    # Правильный ответ
    real_answer = num1 + num2

    # Выводим пример. end="" нужен, чтобы курсор остался в той же строке
    print(f"{num1} + {num2} = ", end="")

    # Получаем ответ от пользователя
    user_answer = input()

    # Превращаем ответ в число.
    if user_answer.isdigit():
        user_answer = int(user_answer)

        if user_answer == real_answer:
            print("Правильно!")
            correct = correct + 1
        else:
            print("Ответ неверный")
            errors = errors + 1
    else:
        print("Нужно вводить цифры! Считаем за ошибку.")
        errors = errors + 1

# Когда цикл закончился (3 ошибки), выводим итог
print(f"\nИгра окончена. Правильных ответов: {correct}")