# Задание.1
n = int(input("Сколько слов вы хотите ввести? "))

# Создаем пустую переменную, куда будем складывать слова
result = ""

# Запускаем цикл, который повторится ровно n раз
for i in range(n):
    word = input(f"Введите слово №{i + 1}: ")

    # Добавляем слово к результату. Если это не первое слово, добавляем перед ним пробел
    if i > 0:
        result = result + " " + word
    else:
        result = result + word
print("Ваша фраза:", result)

# Задание.2
result = ""
first_word = True  # Нужно, чтобы понять, первое ли это слово
print("Вводите слова. Чтобы закончить, напишите 'stop'.")
while True:  # Бесконечный цикл
    word = input("Введите слово: ")

    # Проверяем, не хочет ли пользователь выйти
    if word == "stop":
        break  # Команда break мгновенно останавливает цикл

    # Если это не первое слово, добавляем пробел перед новым словом
    if not first_word:
        result = result + " " + word
    else:
        result = result + word
        first_word = False  # После первого слова меняем на False
print("Ваша фраза:", result)

